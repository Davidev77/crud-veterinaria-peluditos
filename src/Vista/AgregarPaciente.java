    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(!(txtNombre.getText().isEmpty() || txtPeso.getText().isEmpty() || txtEspecie.getText().isEmpty() || txtColor.getText().isEmpty() || txtProcedencia.getText().isEmpty() || txtFinZootecnico.getText().isEmpty() || txtCarPar.getText().isEmpty() || txtEnferBase.getText().isEmpty())){
                nombre = txtNombre.getText();
                peso = Float.parseFloat(txtPeso.getText());
                especie = txtEspecie.getText();
                color = txtColor.getText();
                sexo = (String) cbSexo.getSelectedItem();
                isEsterilizado = cbEsterilizado.getSelectedItem().equals("Si");
                procedencia = txtProcedencia.getText();
                fin_zootecnico = txtFinZootecnico.getText();
                caracteristicas_particulares = txtCarPar.getText();
                enfermedades_base = txtEnferBase.getText();
                
                //JOptionPane.showMessageDialog(this, idPropietario);
                try {
                    paciente = new Paciente(nombre, sexo, especie, peso, color, caracteristicas_particulares, procedencia, fin_zootecnico, isEsterilizado, enfermedades_base, idPropietario);
                    
                    boolean conexion;
                    boolean insert;
                    
                    conexion = bd.crearConexion();
                    if(conexion){
                        insert = bd.insertarPaciente(paciente);
                        if(insert){
                            JOptionPane.showMessageDialog(this, "!!!Se agreg√≥ el Paciente correctamente!!!");
                            txtNombre.setText("");
                            txtPeso.setText("");
                            txtEspecie.setText("");
                            txtPeso.setText("");
                            txtProcedencia.setText("");
                            txtFinZootecnico.setText("");
                            txtCarPar.setText("");
                            txtEnferBase.setText("");
                            setVisible(false);
                        } else{
                            JOptionPane.showMessageDialog(this, "!!!Inserci√≥n fallida!!!");
                        }
                    } else{
                        JOptionPane.showMessageDialog(this, "!!!Conexi√≥n con la Base de Datos Fallida!!!");
                    } 
                } catch (SQLException ex) {
                    Logger.getLogger(GestionarPropietario.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else{
                JOptionPane.showMessageDialog(this, "Por favor ingrese todos los datos");
            }
            
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Tipo de Dato no permitido");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

