    public void encontrarUsuario() throws SQLException{
        String nameTable = "";
        String sqlString = "";
        try {
            Connection conn = bd.getConnection();
            
            nameTable = "propietarios";
            sqlString = "SELECT nombre_completo, telefono, correo_electronico, direccion_residencia FROM "+nameTable+" WHERE numero_documento = ?";
            pstm = conn.prepareStatement(sqlString);
            pstm.setLong(1, identificacion);
            rs = pstm.executeQuery();
            
            if (rs.next()) {
                ctNombreCompleto.setText(rs.getString(1));
                ctTelefono.setText(rs.getString(2));
                ctCorreo.setText(rs.getString(3));
                atDireccion.setText(rs.getString(4));
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no encontrado.");
            }
            
        } catch (SQLException e) {
            System.out.println("Ocurrió un error encontrando este usuario.");
            e.printStackTrace();
        }
    }
    

