    private void btnAgregarPropietarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPropietarioActionPerformed
        try {
            if(!(ctNombreCompleto.getText().isEmpty() || atDireccion.getText().isEmpty() || ctCorreo.getText().isEmpty() || ctTelefono.getText().isEmpty())){
                nombre_completo = ctNombreCompleto.getText();
                direccion = atDireccion.getText();
                correo = ctCorreo.getText();
                telefono = ctTelefono.getText();
                
                try {
                     propietario = new Propietario(identificacion, nombre_completo, direccion, correo, telefono);
                    
                    boolean conexion;
                    boolean insert;

                    conexion = bd.crearConexion();
                    if (conexion) {
                        insert = bd.actualizarPropietario(propietario);
                        if (insert) {
                            JOptionPane.showMessageDialog(this, "!!!Se actualizaron correctamente los datos!!!");
                            setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(this, "!!!Actualización Fallida!!!");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "!!!Conexión con la Base de Datos Fallida!!!");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(EditarPropietario.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else{
                JOptionPane.showMessageDialog(this, "Por favor ingrese todos los datos");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los datos no corresponden con su tipo de dato");
        }

    }//GEN-LAST:event_btnAgregarPropietarioActionPerformed

